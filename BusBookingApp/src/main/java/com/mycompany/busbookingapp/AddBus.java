    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String busnum = txtbusnum.getText();
        String seatno = txtseat.getText();
        String seat_cost = txtcost.getText();
        String dnum = txtdrivernum.getText();

        SimpleDateFormat ft = new SimpleDateFormat("dd-MM-yyyy");
        String dof = ft.format(txtdate.getDate());

        String br = txtroute.getSelectedItem().toString();
        String time = txttime.getText();

        try {
            ps = con.prepareStatement("insert into bus_details values (?, ?, ?, ?, ?, ?, ?)");
            ps.setString(1, busnum);
            ps.setString(2, seatno);
            ps.setString(3, seat_cost);
            ps.setString(4, dnum);
            ps.setString(5, dof);
            ps.setString(6, br);
            ps.setString(7, time);
            ps.executeUpdate();

            ps = con.prepareStatement("insert into bus_features values(?, ?, ?, ?, ?)");
            ps.setString(1, busnum);

            if (txtsleeper.isSelected()) {
                ps.setString(2, "yes");
            } else {
                ps.setString(2, "no");
            }

            if (txtac.isSelected()) {
                ps.setString(3, "yes");
            } else {
                ps.setString(3, "no");
            }

            if (txttv.isSelected()) {
                ps.setString(4, "yes");
            } else {
                ps.setString(4, "no");
            }

            if (txtsnacks.isSelected()) {
                ps.setString(5, "yes");
            } else {
                ps.setString(5, "no");
            }

            ps.executeUpdate();

            ps = con.prepareStatement("Create table `" + busnum + "`(bus_num int, seat INT primary key, status varchar(15))");
            ps.executeUpdate();

            for (int i = 1; i <= Integer.parseInt(seatno); i++) {
                ps = con.prepareStatement("insert into `" + busnum + "` values (?, ?, ?)");
                ps.setString(1, busnum);
                ps.setInt(2, i);
                ps.setString(3, "Available");
                ps.executeUpdate();
            }

        } catch (SQLException ex) {
            Logger.getLogger(AddBus.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

